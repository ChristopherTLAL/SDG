/**
 * Find the CEFR row that contains the given test/score pair.
 * For numeric tests, matches by [min, max] inclusive; for select inputs,
 * matches by string equality on display.
 */
export function matchCefr(testKey: TestKey, score: number | string): CrossWalkRow | null {
  const isNumeric = typeof score === 'number';
  for (const row of CROSS_WALK) {
    const cell = row.scores[testKey];
    if (!cell) continue;
    if (isNumeric) {
      if (cell.min == null || cell.max == null) continue;
      if (score >= cell.min && score <= cell.max) return row;
    } else {
      // string match — Trinity / EIKEN / LangCert / Aptis (select)
      if (cell.display === score || cell.display.includes(score as string)) return row;
    }
  }
  return null;
}
